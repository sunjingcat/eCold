package com.zz.cold.bean;

public class UserInfo {
    String token;
    int role;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getLoginToken() {
        return token;
    }
}
